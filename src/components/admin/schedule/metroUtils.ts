export function getMetroDistance(from: string, to: string): number | null {
  if (from === to) return 0;
  const visited = new Set<string>();
  const queue: [string, number][] = [[from, 0]];
  while (queue.length > 0) {
    const [current, dist] = queue.shift()!;
    if (visited.has(current)) continue;
    visited.add(current);
    const neighbors = METRO_GRAPH[current] || [];
    for (const nb of neighbors) {
      if (nb === to) return dist + 1;
      if (!visited.has(nb)) queue.push([nb, dist + 1]);
    }
  }
  return null;
}

export function extractMetroFromOrgName(orgName: string): string | null {
  const match = orgName.match(/\(([^)]+)\)/);
  return match ? match[1] : null;
}
